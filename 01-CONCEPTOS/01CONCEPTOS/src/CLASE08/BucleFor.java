
package CLASE08;

/**
 *
 * Bucle For -es una estructura iterativa para ejecutar un mismo 
 * segmento de código una cantidad de veces desead
 */
public class BucleFor {
    
    public static void main(String[] args) {
//        for(int i = 0; // índice de control
//        i < 100;   // condición booleana
//        i++)      // modificación del índice tras cada bucle
    
        int valor = 15;
        for (int i = 0; i < valor; i++) {
            System.out.println("Bucle For "+i);
        }
    }
    
}
